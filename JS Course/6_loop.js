// 4 types of loop in js

// 1> while loop
// 2> do while loop
// 3> for loop
// 4> for/in loop

let x = 15;
let y = 1;
while (y <= x) {
  console.log("while loop", y);
  y++;
}

// do while
let xx = 15;
let yy = 1;
do {
  console.log("do while loop", yy);
  yy++;
} while (yy <= xx);

