// 4 types to customize array;

// push , pop, shift and unshift

const arr = [2, 3, 4];
console.log("main array", arr);

arr.push(5);
console.log("after push 5", arr);

arr.pop(); //automatically remove last value of array
arr.pop[1];
console.log("second after pop", arr);

arr.unshift(1);
console.log("after unshift", arr);

arr.shift(10);
console.log("after shift", arr);
